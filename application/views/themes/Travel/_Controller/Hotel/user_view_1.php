<p><span style="color: #000000;">&nbsp; &nbsp; &nbsp; &nbsp;วัดพระทอง</span> นั้นเป็นวัดเก่าแก่ที่มีมาตั้งแต่สมัยกรุงศรีอยุธยาตอนปลาย &nbsp;&nbsp;แม้ไม่ทราบที่มาแน่ชัดเกี่ยวกับที่มาของ<span style="color: #000000;">หลวงพ่อพระผุด</span>นี้ว่าสร้างขึ้นเมื่อใด &nbsp;แต่มีตำนานเก่าแก่เล่ากันว่า พบอยู่ที่ภูเก็ตนี้เนิ่นนานมาแล้ว &nbsp;โดยมีนิทานพื้นบ้านกล่าวว่าครั้งหนึ่งมีเด็กเลี้ยงควายได้เอาควายไปผูกไว้กับ<span style="color: #000000;">พระเกตุมาลา</span>เพราะคิดว่าเป็นไม้ &nbsp;หลังจากที่กลับมาถึงบ้าน &nbsp;ทั้งเด็กและควายก็เสียชีวิตลงโดยไม่ทราบสาเหตุ &nbsp;ต่อมาพ่อของเด็กได้</p>
        
        
          
<p>มีฝันบอกเหตุว่า เป็นเพราะบุตรชายได้เอาควายไปผูกไว้ที่พระเกตุมาลาของพระพุทธรูป &nbsp;ในตอนรุ่งเช้าไปดูเข้าจึงรู้ว่าเป็นพระพุทธรูปที่จมอยู่ในดิน &nbsp;แต่ขุดขึ้นมาได้เพียงแค่ครึ่งองค์เท่านั้น &nbsp;ภายหลังมี<span style="color: #000000;">พระธุดงค์</span>มาปักกลดบริเวณนั้นแล้วพบเข้าจึงได้สร้างวัดแห่งนี้ขึ้น<br />&nbsp; &nbsp; &nbsp; มีเรื่องเล่าอีกว่าเมื่อพระเจ้าปะดุงยกทัพพม่ามาตีเมืองถลางนั้น &nbsp;ทหารพม่าพยายามขุดองค์พระผุดขึ้นมาจากพื้นดิน &nbsp;เพื่อนำกลับไปพม่าแต่ไม่สำเร็จ &nbsp;เพราะขุดลงไปคราใดก็มีฝูงมดและแตนไล่ต่อยจนต้องละความพยายาม ประจวบกับที่กองทัพไทยยกมาช่วยเมืองถลางจึงล่าถอยไป &nbsp;มีคำกล่าวอ้างกันว่าสาเหตุที่เรียก<span style="color: #000000;">พระผุด</span>ว่า<span style="color: #000000;">พระทอง</span>อีกนามหนึ่งนั้น &nbsp;เนื่องจากครั้งหนึ่ง<span style="color: #000000;">พระครูวิตถารสมณวัตร์</span>ซึ่งเป็นเจ้าอาวาสรูปเดิมในอดีต &nbsp;ได้ทำการบูรณปฏิสังขรณ์วัดและพระผุดเนื่องจากเกรงว่าจะทลายลง &nbsp;เมื่อได้เอาเหล็กแหลมดอกเข้าไปใต้ฐานพระพุทธรูปนั้น</p>
       
          
<p>มีเนื้อเป็นทองคำ &nbsp;อยู่ด้านในแต่ก็ได้พอกปูนทับลงไปเช่นเดิม &nbsp;ดังที่เห็นในปัจจุบันหากคำลือเลื่องนั้นได้แพร่หลายออกไปว่าพระผุดคือพระทองคำจึงเป็นที่มาของชื่อพระทอง &nbsp;และวัดพระทองนั่นเอง &nbsp;ปัจจุบันบริเวณวัดพระทองแห่งนี้ยังมีพิพิธภัณฑ์พื้นบ้าน &nbsp;ที่แสดงข้าวของเครื่องใช้ที่แสดงถึงชีวิตความเป็นอยู่ของชุมชนในอดีตให้ได้ชมด้วย</p>
<p><span style="color: #ff6600;">ความเชื่อและวิธีการบูชา</span><br />&nbsp; &nbsp; &nbsp; พระผุดหรือพระทองเป็นที่เคารพและศรัทธาของชาวภูเก็ตเป็นอย่างมาก &nbsp;เชื่อว่าหากใครมากราบไหว้ของสิ่งใดก็จะสัมฤทธิ์ผล สมความปรารถนา &nbsp;จะมีเงินมีทองไหลมาเทมา<br /><span style="color: #ff6600;">สถานที่ตั้ง</span><br />บ้านนาใน &nbsp;ต.เทพกษัตรี &nbsp;อ.ถลาง &nbsp;จ.ภูเก็ต</p>        
 
              
